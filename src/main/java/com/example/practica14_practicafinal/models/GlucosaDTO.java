package com.example.practica14_practicafinal.models;

public class GlucosaDTO {
    private String gmail;
    private double nivelGlucosa;
    private String notas;

    public String getGmail() {
        return gmail;
    }

    public void setGmail(String gmail) {
        this.gmail = gmail;
    }

    public double getNivelGlucosa() {
        return nivelGlucosa;
    }

    public void setNivelGlucosa(double nivelGlucosa) {
        this.nivelGlucosa = nivelGlucosa;
    }
    public String getNotas() {
        return notas;
    }
    public void setNotas(String notas) {
        this.notas = notas;
    }
}
